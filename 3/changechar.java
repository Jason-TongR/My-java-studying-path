public class changechar{
	public static void main(String[] args){
		System.out.println("ab\tcd\tef");
		System.out.println("jack\nsmith\nmary");
		System.out.println("hello\\milan    hello\\\\milan");
		System.out.println("Jason says \"we need to study hard.\"");
		System.out.println("Jason\rjava");     //  '\r' is back to the first 'J'
		System.out.println("Jason\r\njava");
	}
}

